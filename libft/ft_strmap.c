#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		i;
	int		s_size;

	i = 0;
	s_size = ft_strlen((char*)s);
	if (!(str = (char*)malloc(sizeof(char) * s_size + 1)))
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
